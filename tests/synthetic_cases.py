def test_engine_resolves_hand_crafted_eligible_case() -> None:
    """
    Hand-crafted eligible scenario — always runs regardless of synthetic cases file.

    Income: $2,400/month. FMR: $1,600. 30% of income = $720.
    Housing cost: $1,200 > $720 → financial need met.
    Expected max_award = $1,600 - $720 = $880.
    """
    graph = build_fema_ia_graph()
    engine = FactGraphEngine(graph)
    engine.load_facts({
        "primary_residence_damaged_by_disaster": True,
        "primary_residence_is_uninhabitable": True,
        "primary_residence_is_inaccessible": False,
        "made_unavailable_by_landlord": False,
        "has_permanent_housing_plan": True,
        "gross_post_disaster_monthly_income": 2400.0,
        "monthly_housing_cost": 1200.0,
        "fair_market_rent_amount": 1600.0,
        "in_special_flood_hazard_area": False,
        "in_nfip_participating_community": True,
        "insurance_covers_housing_need": False,
        "other_source_provides_assistance": False,
        "flood_insurance_previously_required": False,
        "flood_insurance_previously_maintained": False,
    })
    engine.resolve()
    det = engine.get_determination()

    eligible = det.terminal_facts.get("applicant_eligible_for_rental_assistance")
    assert eligible is not None
    assert eligible.value is True

    award = det.terminal_facts.get("maximum_award_amount")
    assert award is not None
    assert award.value == pytest.approx(880.0)  # 1600 - 720


def test_engine_resolves_hand_crafted_ineligible_case() -> None:
    """
    Hand-crafted ineligible scenario: insurance covers the housing need
    → duplication of benefits → ineligible.
    """
    graph = build_fema_ia_graph()
    engine = FactGraphEngine(graph)
    engine.load_facts({
        "primary_residence_damaged_by_disaster": True,
        "primary_residence_is_uninhabitable": True,
        "primary_residence_is_inaccessible": False,
        "made_unavailable_by_landlord": False,
        "has_permanent_housing_plan": True,
        "gross_post_disaster_monthly_income": 2000.0,
        "monthly_housing_cost": 1500.0,
        "fair_market_rent_amount": 1400.0,
        "in_special_flood_hazard_area": False,
        "in_nfip_participating_community": True,
        "insurance_covers_housing_need": True,  # ← duplicates benefits
        "other_source_provides_assistance": False,
        "flood_insurance_previously_required": False,
        "flood_insurance_previously_maintained": False,
    })
    engine.resolve()
    det = engine.get_determination()

    duplication_trace = det.resolved_facts.get("duplication_of_benefits_clear")
    assert duplication_trace is not None
    assert duplication_trace.value is False

    eligible = det.terminal_facts.get("applicant_eligible_for_rental_assistance")
    assert eligible is not None
    assert eligible.value is False
